    <h1>CodeIgnitor Aubio Interface</h1>

    <p>A web based GUI for Aubiocut built with Codeigniter and Jquery</p>

    <h2>Requirements</h2>

    <ul>
      <li>Codeigniter 2.0.2</li>
      <li>Aubiotools</li>
      <li>Gnuplot</li>
    </ul>

    <h2>Installation</h2>

    <ul>
      <li>Tested under Ubuntu 10.10</li>
      <li>Download latest version of Codeigniter</li>
      <li>Copy the applications and assets folder to the root Codeigniter directory</li>
      <li>Add 'uploads' folder to root directory with 777 permissions</li>
      <li>Install aubio-tools and gnuplot
        <pre>sudo apt-get install aubio-tools python-gnuplot</pre>
      </li>
      <li>This should install all required packages on Ubuntu/Debian machine</li>
    </ul>

